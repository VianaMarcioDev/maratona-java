package br.com.marcioviana.introducao;

public class Aula06EstruturasDeRepeticao {
    public static void main(String[] args) {

        // Laço while
        int contador = 0;
        while (contador < 10) {
            System.out.println(contador);
            contador++;
        }

        // Laço Do While
        do{
            System.out.println("Eita! Começou!");
        }while(contador < 10);{
            System.out.println(contador);
            contador++;
        }

        // Laço for
        for (int i = 0; i < 10; i++) {
            System.out.println(contador);
            contador++;
        }


    }
}
